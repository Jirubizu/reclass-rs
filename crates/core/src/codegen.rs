#[allow(clippy::too_many_arguments)]
fn emit_scalar_array(
    out: &mut String,
    get: &str,
    set: &str,
    ty: &str,
    width: usize,
    count: usize,
    off: usize,
    field: &str,
) {
    use std::fmt::Write;
    let total = width * count;
    let _ = writeln!(out, "    /// `{field}`: [{ty}; {count}] @ 0x{off:X}");
    let _ = writeln!(
        out,
        "    pub fn {get}(&self) -> Result<[{ty}; {count}], Error> {{"
    );
    let _ = writeln!(out, "        let mut b = [0u8; {total}];");
    let _ = writeln!(
        out,
        "        self.proc.read_bytes(self.base + 0x{off:X}, &mut b)?;"
    );
    let _ = writeln!(
        out,
        "        Ok(std::array::from_fn(|i| {ty}::from_le_bytes("
    );
    let _ = writeln!(
        out,
        "            b[i * {width}..i * {width} + {width}].try_into().unwrap(),"
    );
    let _ = writeln!(out, "        )))");
    out.push_str("    }\n");
    let _ = writeln!(
        out,
        "    pub fn {set}(&self, value: &[{ty}; {count}]) -> Result<(), Error> {{"
    );
    let _ = writeln!(out, "        let mut b = [0u8; {total}];");
    let _ = writeln!(out, "        for (i, v) in value.iter().enumerate() {{");
    let _ = writeln!(
        out,
        "            b[i * {width}..i * {width} + {width}].copy_from_slice(&v.to_le_bytes());"
    );
    let _ = writeln!(out, "        }}");
    let _ = writeln!(
        out,
        "        self.proc.write_bytes(self.base + 0x{off:X}, &b)"
    );
    out.push_str("    }\n");
}

